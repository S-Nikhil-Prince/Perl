#!/usr/bin/perl
use strict;
use warnings;

# Arithmetic Operations in Perl

my $a = 10;
my $b = 3;

# Addition
my $sum = $a + $b;
print "Addition: $a + $b = $sum\n";

# Subtraction
my $difference = $a - $b;
print "Subtraction: $a - $b = $difference\n";

# Multiplication
my $product = $a * $b;
print "Multiplication: $a * $b = $product\n";

# Division
my $quotient = $a / $b;
print "Division: $a / $b = $quotient\n";

# Modulus (Remainder)
my $remainder = $a % $b;
print "Modulus: $a % $b = $remainder\n";

# Exponentiation
my $power = $a ** $b;
print "Exponentiation: $a ** $b = $power\n";

# Integer Division
my $int_division = int($a / $b);
print "Integer Division: int($a / $b) = $int_division\n";

